/** Merge a {en:{...}, zh:{...}} fragment into the dictionary (page modules call this). */
export function registerDict(fragment) {
  for (const lang of SUPPORTED) {
    if (fragment[lang]) Object.assign(DICT[lang], fragment[lang]);
  }
}

export function getLang() {
  return _lang;
}

export function setLang(lang) {
  if (!SUPPORTED.includes(lang) || lang === _lang) return;
  _lang = lang;
  try {
    localStorage.setItem(STORAGE_KEY, lang);
  } catch (_) {
    /* ignore */
  }
  if (typeof document !== "undefined") document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  for (const fn of _subs) {
    try {
      fn(lang);
    } catch (err) {
      console.error("i18n subscriber error", err);
    }
  }
}

export function toggleLang() {
  setLang(_lang === "en" ? "zh" : "en");
}

export function onLang(fn) {
  _subs.add(fn);
  return () => _subs.delete(fn);
}

/** Translate a key with optional {var} interpolation; falls back en -> key. */
export function t(key, vars) {
  let s = (DICT[_lang] && DICT[_lang][key]) ?? DICT.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) s = s.replaceAll("{" + k + "}", String(v));
  }
  return s;
}

